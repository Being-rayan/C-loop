// 2 values side b y side.
#include <stdio.h>
int main () {
    int i=0,j=1; // can decalare in starting also.
    for (;j<10,i<=5;i++,j++) { //jo last me rehta hai termination condn ka whi tk chlega.
        printf("i=%d,j=%d\n",i,j); // \n tbhi kaam krta hai jb dono value print ho jae.
    }

    return 0;
}